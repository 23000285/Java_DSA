package Bitwise_Operator;

public class Swap_of_Variable {
    public static void main(String[] args){
        int a=10;
        int b=20;

        // 10 -> 0000 1010  20 -> 0001 0100

        a=a^b; 
        // 10 -> 0000 1010
        // 20 -> 0001 0100
        // ------------------
        //  1 -> 0000 0001
        // ------------------

        b=a^b;
        //  1 -> 0000 0001
        // 20 -> 0001 0100
        // ------------------
        // 10 -> 0000 1010
        // ------------------
        
        a=a^b;
        //  1 -> 0000 0001
        // 10 -> 0000 1010
        // ------------------
        // 20 -> 0001 0100
        // ------------------

        System.out.println(a+" "+b);
    }
}
